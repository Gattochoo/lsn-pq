#!/usr/bin/env python3
"""R2a: LSN sample-complexity brute-force for n=4,5 via transvection-orbit enumeration."""
import json
import random
import time
import numpy as np


def symplectic_form(x, y, n):
    """Symplectic form Omega(x,y) over F_2^{2n}."""
    s = 0
    for i in range(n):
        a0 = (x >> (2 * i)) & 1
        a1 = (x >> (2 * i + 1)) & 1
        b0 = (y >> (2 * i)) & 1
        b1 = (y >> (2 * i + 1)) & 1
        s ^= (a0 * b1) ^ (a1 * b0)
    return s


def transvection(v, x, n):
    """Symplectic transvection T_v(x) = x + Omega(x,v) * v."""
    return x ^ (symplectic_form(x, v, n) * v)


def apply_transvection_to_set(L, v, n):
    """Apply transvection T_v to every element of Lagrangian L."""
    return frozenset(transvection(v, x, n) for x in L)


def get_symplectic_generators(n):
    """Generate a small generating set for Sp(2n, F_2) via transvections.

    Sp(2n) is generated by transvections T_v where v runs over:
      - e_i, f_i  (2n vectors)
      - e_i + e_j  (i < j)
      - f_i + f_j  (i < j)
      - e_i + f_j  (all i, j)
    """
    generators = []
    # e_i and f_i
    for i in range(n):
        generators.append(1 << (2 * i))       # e_i
        generators.append(1 << (2 * i + 1))   # f_i
    # e_i + e_j
    for i in range(n):
        for j in range(i + 1, n):
            generators.append((1 << (2 * i)) | (1 << (2 * j)))
    # f_i + f_j
    for i in range(n):
        for j in range(i + 1, n):
            generators.append((1 << (2 * i + 1)) | (1 << (2 * j + 1)))
    # e_i + f_j
    for i in range(n):
        for j in range(n):
            generators.append((1 << (2 * i)) | (1 << (2 * j + 1)))
    return generators


def enumerate_lagrangians_transvection(n):
    """Enumerate all Lagrangians in F_2^{2n} via transvection BFS."""
    total_dim = 2 * n
    # Standard Lagrangian L0 = span{e_1, e_2, ..., e_n}
    L0_list = [0]
    for i in range(n):
        vec = 1 << (2 * i)
        L0_list = L0_list + [x ^ vec for x in L0_list]
    L0 = frozenset(L0_list)

    generators = get_symplectic_generators(n)
    orbit = {L0}
    queue = [L0]
    visited = 0
    t0 = time.time()
    while queue:
        L = queue.pop()
        visited += 1
        if visited % 1000 == 0:
            elapsed = time.time() - t0
            print(f"  BFS: visited {visited}, orbit size {len(orbit)}, {elapsed:.1f}s")
        for v in generators:
            L_new = apply_transvection_to_set(L, v, n)
            if L_new not in orbit:
                orbit.add(L_new)
                queue.append(L_new)
    return list(orbit)


def build_membership_table(lagrangians, total_dim):
    """Build a NumPy boolean membership table: table[i, a] = (a in L_i)."""
    num = len(lagrangians)
    size = 1 << total_dim
    table = np.zeros((num, size), dtype=bool)
    for i, L in enumerate(lagrangians):
        for a in L:
            table[i, a] = True
    return table


def run_trials(n, m, num_trials, p, lagrangians, membership):
    """Run brute-force decoder trials."""
    total_dim = 2 * n
    num_lagr = len(lagrangians)
    size = 1 << total_dim
    success = 0

    for t in range(num_trials):
        L_idx = random.randrange(num_lagr)
        # Generate m samples
        a_vals = np.random.randint(0, size, size=m)
        noise = (np.random.random(m) < p).astype(bool)
        pred = membership[L_idx, a_vals]
        b_vals = pred ^ noise

        # Brute-force: find Lagrangian with highest agreement
        scores = np.zeros(num_lagr, dtype=np.int32)
        for j in range(m):
            scores += (membership[:, a_vals[j]] == b_vals[j])
        best_idx = np.argmax(scores)
        if best_idx == L_idx:
            success += 1
    return success / num_trials


def main():
    random.seed(42)
    np.random.seed(42)
    p = 0.25
    all_results = {}

    for n in [4, 5]:
        print(f"\n=== n={n} ===")
        t0 = time.time()
        lagrangians = enumerate_lagrangians_transvection(n)
        elapsed = time.time() - t0
        expected = 1
        for i in range(1, n + 1):
            expected *= (2**i + 1)
        print(f"  Enumerated {len(lagrangians)} Lagrangians in {elapsed:.1f}s (expected {expected})")
        assert len(lagrangians) == expected, f"Mismatch: {len(lagrangians)} != {expected}"

        print("  Building membership table...")
        membership = build_membership_table(lagrangians, 2 * n)
        print(f"  Table shape: {membership.shape}, memory: {membership.nbytes / 1e6:.1f} MB")

        base = 2 ** (2 * n)
        m_values = [int(base * r) for r in [0.25, 0.5, 1.0, 2.0, 4.0]]
        if n == 5:
            # Reduce trials for n=5 to keep runtime manageable
            num_trials_list = [20, 20, 20, 20, 20]
        else:
            num_trials_list = [50, 50, 50, 50, 50]
        print(f"  Testing m values: {m_values} with trials: {num_trials_list}")

        success_rates = []
        for m, num_trials in zip(m_values, num_trials_list):
            t0 = time.time()
            rate = run_trials(n, m, num_trials, p, lagrangians, membership)
            elapsed = time.time() - t0
            success_rates.append(rate)
            print(f"    m={m:6d} (ratio={m/base:.2f}*2^{{2n}}): success={rate:.2%} ({elapsed:.1f}s)")

        all_results[n] = {
            "m_values": m_values,
            "success_rates": success_rates,
            "num_trials": num_trials_list,
            "p": p,
            "num_lagrangians": len(lagrangians)
        }

    out = "/Users/gatto/projects/TRIARC-main/.claude/worktrees/hardness-7th-shared/lsn-experiments/73-lsn-sample-complexity-n4n5-results.json"
    with open(out, "w") as f:
        json.dump(all_results, f, indent=2)
    print(f"\nResults saved to {out}")


if __name__ == "__main__":
    main()
