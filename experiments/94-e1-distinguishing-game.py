#!/usr/bin/env python3
"""
P1 E1: Single-sample distinguishing game for Open Problem 9.
Measures 4 statistics on P0 (symplectic LPN with adaptive B) vs P1 (standard LPN).

P0: isotropic basis A; random B; w = Ax + e (e~Ber(1/4)); output (C,y) = (BA, Bw).
P1: uniform C'; x' uniform; y' = C'x' + e' (e'~Ber(p'), p' in {0.1,0.2}).

Statistics (per sample):
  (a) Syndrome weight: Hamming weight of y mod colspace(C)
  (b) [C|y] rank
  (c) 2nd-moment proxy: empirical pair-correlation of y
  (d) Max-agreement: max_x #{i: y_i = c_i · x}

Output: JSON with measured separation per (n, m, p', B_family, stat).
"""

import random
import json
import sys
from itertools import combinations

# --- F_2 linear algebra helpers ---

def gf2_rank(matrix_rows):
    """Rank of binary matrix given as list of row bitmasks."""
    m = list(matrix_rows)
    rank = 0
    row = 0
    if not m:
        return 0
    col = max((x.bit_length() for x in m), default=0)
    for c in range(col - 1, -1, -1):
        pivot = None
        for r in range(row, len(m)):
            if (m[r] >> c) & 1:
                pivot = r
                break
        if pivot is None:
            continue
        m[row], m[pivot] = m[pivot], m[row]
        for r in range(len(m)):
            if r != row and ((m[r] >> c) & 1):
                m[r] ^= m[row]
        row += 1
        rank += 1
        if row >= len(m):
            break
    return rank


def dot_parity(a, b):
    """Parity of bitwise AND (F_2 dot product)."""
    return (a & b).bit_count() & 1


def syndrome_weight(C, y):
    """
    Hamming weight of syndrome of y w.r.t. code generated by C.
    C: list of m row bitmasks (n bits each)
    y: m-bit integer
    Returns: int in [0, m - rank(C)]
    """
    m = len(C)
    if m == 0:
        return 0
    n = max((x.bit_length() for x in C), default=0)

    # Augmented matrix [C | y]; column n is the y column
    aug = list(C)
    for i in range(m):
        if (y >> i) & 1:
            aug[i] |= (1 << n)

    # Gaussian elimination on first n columns
    rank = 0
    row = 0
    for c in range(n - 1, -1, -1):
        pivot = None
        for r in range(row, m):
            if (aug[r] >> c) & 1:
                pivot = r
                break
        if pivot is None:
            continue
        aug[row], aug[pivot] = aug[pivot], aug[row]
        for r in range(m):
            if r != row and ((aug[r] >> c) & 1):
                aug[r] ^= aug[row]
        row += 1
        rank += 1
        if row >= m:
            break

    # Syndrome bits are in zero-rows at column n
    weight = 0
    for i in range(rank, m):
        if (aug[i] >> n) & 1:
            weight += 1
    return weight


def rank_Cy(C, y):
    """Rank of augmented matrix [C | y]."""
    m = len(C)
    if m == 0:
        return 0
    n = max((x.bit_length() for x in C), default=0)
    aug = list(C)
    for i in range(m):
        if (y >> i) & 1:
            aug[i] |= (1 << n)
    return gf2_rank(aug)


def second_moment_proxy(y, m):
    """
    Empirical pair-correlation: average of (2*y_i-1)(2*y_j-1) over all pairs.
    For a single m-bit vector y.
    """
    w = y.bit_count()
    if m <= 1:
        return 0.0
    # Under independence with marginal p=w/m, expected pair-product is (2p-1)^2
    # Observed: count agreeing pairs / total pairs, scaled
    total_pairs = m * (m - 1) // 2
    if total_pairs == 0:
        return 0.0
    one_pairs = w * (w - 1) // 2
    zero_pairs = (m - w) * (m - w - 1) // 2
    # Fraction of pairs with same value
    agree = (one_pairs + zero_pairs) / total_pairs
    # Map to centered product: agree = (1 + rho)/2  =>  rho = 2*agree - 1
    return 2.0 * agree - 1.0


def max_agreement(C, y):
    """
    max_x #{i : y_i = dot_parity(C[i], x)}.
    Brute force over all x in F_2^n.
    Returns: int in [0, m]
    """
    m = len(C)
    if m == 0:
        return 0
    n = max((x.bit_length() for x in C), default=0)
    best = 0
    for x in range(1 << n):
        cnt = sum(1 for i in range(m) if dot_parity(C[i], x) == ((y >> i) & 1))
        if cnt > best:
            best = cnt
    return best


# --- P0 / P1 sample generators ---

def random_symmetric_matrix_rows(n):
    """Return n×n symmetric matrix over F_2 as list of n row bitmasks."""
    rows = []
    for i in range(n):
        row = random.getrandbits(n)
        # Enforce symmetry for j < i
        for j in range(i):
            if (rows[j] >> i) & 1:
                row |= (1 << j)
            else:
                row &= ~(1 << j)
        rows.append(row)
    return rows


def isotropic_basis_from_symmetric(M_rows, n):
    """
    Graph Lagrangian basis A (2n × n) for L_M.
    Returns list of 2n integers (each n bits).
    """
    A = []
    for j in range(n):
        # a_j row: x_j coefficient
        A.append(1 << j)
        # b_j row: (Mx)_j coefficients
        b_row = 0
        for i in range(n):
            if (M_rows[j] >> i) & 1:
                b_row |= (1 << i)
        A.append(b_row)
    return A


def sample_P0(n, m, p, B_family="uniform"):
    """
    Generate one sample from P0.
    Returns (C, y) where C is m×n, y is m-bit int.
    """
    # Random symmetric M -> isotropic basis A (2n × n)
    M_rows = random_symmetric_matrix_rows(n)
    A = isotropic_basis_from_symmetric(M_rows, n)

    # Secret x
    x = random.getrandbits(n)

    # Noise e (2n bits)
    e = 0
    for k in range(2 * n):
        if random.random() < p:
            e |= (1 << k)

    # w = Ax + e  (2n-bit vector)
    w = 0
    for k in range(2 * n):
        if (dot_parity(A[k], x) ^ ((e >> k) & 1)):
            w |= (1 << k)

    # B (m × 2n)
    if B_family == "uniform":
        B = [random.getrandbits(2 * n) for _ in range(m)]
    elif B_family == "low_weight":
        B = []
        for _ in range(m):
            # Each row has Hamming weight ~ 3 (constant, low)
            row = 0
            positions = random.sample(range(2 * n), min(3, 2 * n))
            for pos in positions:
                row |= (1 << pos)
            B.append(row)
    elif B_family == "identity_first":
        # B = [I_{m} | 0] if m <= 2n, else pad
        B = []
        for i in range(m):
            if i < 2 * n:
                B.append(1 << i)
            else:
                B.append(random.getrandbits(2 * n))
    else:
        raise ValueError(f"Unknown B_family: {B_family}")

    # C = BA (m × n): XOR selected rows of A
    C = []
    for i in range(m):
        c_row = 0
        for k in range(2 * n):
            if (B[i] >> k) & 1:
                c_row ^= A[k]
        C.append(c_row)

    # y = Bw (m-bit)
    y = 0
    for i in range(m):
        if dot_parity(B[i], w):
            y |= (1 << i)

    return C, y


def sample_P1(m, n, p_prime):
    """
    Generate one sample from P1 (standard LPN).
    Returns (C_prime, y_prime).
    """
    C_prime = [random.getrandbits(n) for _ in range(m)]
    x_prime = random.getrandbits(n)
    y_prime = 0
    for i in range(m):
        val = dot_parity(C_prime[i], x_prime)
        if random.random() < p_prime:
            val ^= 1
        if val:
            y_prime |= (1 << i)
    return C_prime, y_prime


# --- Experiment runner ---

def run_distinguishing_game(n, m, p_prime, B_family, num_samples=2000):
    """
    Generate num_samples from P0 and P1 each.
    Compute 4 statistics. Return distributions.
    """
    stats_P0 = {"syndrome": [], "rank_diff": [], "corr": [], "max_agree": []}
    stats_P1 = {"syndrome": [], "rank_diff": [], "corr": [], "max_agree": []}

    for _ in range(num_samples):
        # P0
        C0, y0 = sample_P0(n, m, p=0.25, B_family=B_family)
        r0 = gf2_rank(C0)
        stats_P0["syndrome"].append(syndrome_weight(C0, y0))
        stats_P0["rank_diff"].append(rank_Cy(C0, y0) - r0)
        stats_P0["corr"].append(second_moment_proxy(y0, m))
        stats_P0["max_agree"].append(max_agreement(C0, y0))

        # P1
        C1, y1 = sample_P1(m, n, p_prime)
        r1 = gf2_rank(C1)
        stats_P1["syndrome"].append(syndrome_weight(C1, y1))
        stats_P1["rank_diff"].append(rank_Cy(C1, y1) - r1)
        stats_P1["corr"].append(second_moment_proxy(y1, m))
        stats_P1["max_agree"].append(max_agreement(C1, y1))

    return stats_P0, stats_P1


def summarize_dist(values):
    """Return mean, std, min, max, median."""
    values = sorted(values)
    N = len(values)
    mean = sum(values) / N
    var = sum((v - mean) ** 2 for v in values) / N
    std = var ** 0.5
    median = values[N // 2] if N % 2 == 1 else (values[N // 2 - 1] + values[N // 2]) / 2
    return {
        "mean": round(mean, 4),
        "std": round(std, 4),
        "min": min(values),
        "max": max(values),
        "median": round(median, 4),
    }


def separation_score(P0_vals, P1_vals):
    """
    Simple separation: how many standard deviations apart are the means.
    Returns (delta_mean, pooled_std, separation_ratio).
    """
    m0 = sum(P0_vals) / len(P0_vals)
    m1 = sum(P1_vals) / len(P1_vals)
    v0 = sum((v - m0) ** 2 for v in P0_vals) / len(P0_vals)
    v1 = sum((v - m1) ** 2 for v in P1_vals) / len(P1_vals)
    pooled_std = ((v0 + v1) / 2) ** 0.5
    delta = abs(m0 - m1)
    ratio = delta / pooled_std if pooled_std > 0 else float('inf')
    return round(delta, 4), round(pooled_std, 4), round(ratio, 4)


def main():
    random.seed(0x5E1F)
    results = []

    configs = [
        # (n, m, p_prime, B_family)
        (4, 8, 0.2, "uniform"),
        (4, 16, 0.2, "uniform"),
        (5, 10, 0.2, "uniform"),
        (5, 20, 0.2, "uniform"),
        (6, 12, 0.2, "uniform"),
        (6, 24, 0.2, "uniform"),
        (4, 8, 0.2, "low_weight"),
        (5, 10, 0.2, "low_weight"),
        (6, 12, 0.2, "low_weight"),
    ]

    NUM_SAMPLES = 2000

    for n, m, p_prime, B_family in configs:
        print(f"Running n={n}, m={m}, p'={p_prime}, B={B_family} ...", file=sys.stderr)
        stats_P0, stats_P1 = run_distinguishing_game(n, m, p_prime, B_family, NUM_SAMPLES)

        entry = {
            "n": n,
            "m": m,
            "p_prime": p_prime,
            "B_family": B_family,
            "num_samples": NUM_SAMPLES,
            "P0": {},
            "P1": {},
            "separation": {},
        }

        for stat_name in ["syndrome", "rank_diff", "corr", "max_agree"]:
            entry["P0"][stat_name] = summarize_dist(stats_P0[stat_name])
            entry["P1"][stat_name] = summarize_dist(stats_P1[stat_name])
            delta, pooled, ratio = separation_score(stats_P0[stat_name], stats_P1[stat_name])
            entry["separation"][stat_name] = {
                "delta_mean": delta,
                "pooled_std": pooled,
                "ratio": ratio,
            }

        results.append(entry)

    output = {
        "experiment": "94-e1-distinguishing-game",
        "description": "P0 vs P1 single-sample 4-statistic distinguishing",
        "p0_noise": 0.25,
        "results": results,
    }

    with open("experiments/94-e1-results.json", "w") as f:
        json.dump(output, f, indent=2)

    print("Saved experiments/94-e1-results.json", file=sys.stderr)

    # Print quick summary table
    print("\n=== SUMMARY ===", file=sys.stderr)
    print("n | m | B_family      | syndrome | rank_diff | corr    | max_agree", file=sys.stderr)
    for entry in results:
        seps = entry["separation"]
        print(f"{entry['n']} | {entry['m']:2} | {entry['B_family']:13} | "
              f"{seps['syndrome']['ratio']:8.3f} | {seps['rank_diff']['ratio']:9.3f} | "
              f"{seps['corr']['ratio']:7.3f} | {seps['max_agree']['ratio']:9.3f}", file=sys.stderr)


if __name__ == "__main__":
    main()
